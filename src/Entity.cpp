//-----------------------------------------------------------------------------
// 생성자 
//-----------------------------------------------------------------------------
CEntity::CEntity(LPDIRECT3DDEVICE9 _pDevice)
{
	m_isRotating = false;


	D3DXMatrixIdentity(&m_MWorld);
	Init(_pDevice);

	D3DXMatrixIdentity(&m_MWorld);

	D3DXMatrixIdentity(&m_MTrans);
	D3DXMatrixIdentity(&m_MRot);

	D3DXMatrixIdentity(&m_MRotPitch);
	D3DXMatrixIdentity(&m_MRotRoll);
	D3DXMatrixIdentity(&m_MRotYaw);

}

//-------------------------------------------------------------------------
// 소멸자
//-------------------------------------------------------------------------
CEntity::~CEntity()
{
	
}


//-----------------------------------------------------------------------------
// 초기화 함수
//-----------------------------------------------------------------------------
HRESULT CEntity::Init(LPDIRECT3DDEVICE9 _pDevice)
{	
	if(FAILED(CGameObject::SetDevice(_pDevice)))		
	{
		return S_FALSE;
	}
	
	m_Pos = D3DXVECTOR3(0,0,0);
	m_PrePos= D3DXVECTOR3(0,0,0); 

	return S_OK;

}


